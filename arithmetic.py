def add(num1, num2):
    return num1 + num2

def subtract(num1, num2):
    return num1 - num2
    
#subtract(8,3)

def multiply(num1, num2):
    return num1 * num2

def divide(num1, num2):
    return num1 / num2

def square(num1):
    return num1 * num1

#print square(5)

def cube(num1):
    return num1 ** 3

#print cube(3)

def power(num1, num2):
    return num1 ** num2
#print power(2, 3)

def mod(num1, num2):
    return num1 % num2

